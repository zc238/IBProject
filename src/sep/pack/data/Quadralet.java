package sep.pack.data;

public class Quadralet{
	public Quadralet(double a1, double b1, double c1, double d1){
		a = a1; b = b1; c = c1; d = d1;
	}
	private double a; 
	private double b; 
	private double c; 
	private double d;
	public double getA() {
		return a;
	}
	public double getB() {
		return b;
	}
	public double getC() {
		return c;
	}
	public double getD() {
		return d;
	}
	
}
